public class FloatDouble {
    public static void main(String[] args) {
        float radius = 10.8f;
        float pi = 3.14f;
        float area = pi * radius * radius;
        System.out.printf("• area = %f \n", area);

        int helen = 47;
        int max = 17;
        int john = 13;
        int nick = 22;
        int sum = helen + max + john + nick;
        double avg = sum / 4.0;
        System.out.printf("• avg = %f \n", avg);
    }
}
